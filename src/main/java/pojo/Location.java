package pojo;

// pojo class= to be consumed in AddPlace pojo class
public class Location {

    //to generate setters and getters shortcut : alt + shift + s
    private double lat;
    private double lng;
    
    public double getLat() {
        return lat;
    }
    public void setLat(double lat) {
        this.lat = lat;
    }
    public double getLng() {
        return lng;
    }
    public void setLng(double lng) {
        this.lng = lng;
    }
  

}
